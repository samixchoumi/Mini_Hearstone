package mecanique.adversaire;

public class Adversaire {
	private String name;
	private int life;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getLife() {
		return this.life;
	}
	
	public void setLife(int life) {
		this.life = life;
	}
}
